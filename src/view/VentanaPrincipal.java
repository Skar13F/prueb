    @Override
    public void actionPerformed(ActionEvent ae) {
        Object evt=ae.getSource();
        if(evt.equals(boton_Lib)){
            panelLib.setVisible(true);
            panelAlum.setVisible(false);
            panelEnc.setVisible(false);
            panelPrest.setVisible(false);
            contenedor.add(panelLib);
            contenedor.validate();
            deshabilitarBotones();
        }else if(evt.equals(boton_Alum)){
            panelLib.setVisible(false);
            panelAlum.setVisible(true);
            panelEnc.setVisible(false);
            panelPrest.setVisible(false);
            contenedor.add(panelAlum);
            contenedor.validate();
            deshabilitarBotones();
        }else if(evt.equals(boton_Enc)){
            panelLib.setVisible(false);
            panelAlum.setVisible(false);
            panelEnc.setVisible(true);
            panelPrest.setVisible(false);
            contenedor.add(panelEnc);
            contenedor.validate();
            deshabilitarBotones();
        }else if(evt.equals(boton_Prest)){
            panelLib.setVisible(false);
            panelAlum.setVisible(false);
            panelEnc.setVisible(false);
            panelPrest.setVisible(true);
            contenedor.add(panelPrest);
            contenedor.validate();
            deshabilitarBotones();
        }
    }
