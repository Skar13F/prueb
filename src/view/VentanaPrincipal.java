    private void deshabilitarBotones(){
        if(panelLib.isVisible()){
            boton_Lib.setEnabled(false);
            boton_Alum.setEnabled(true);
            boton_Enc.setEnabled(true);
            boton_Prest.setEnabled(true);
        }else if(panelAlum.isVisible()){
            boton_Lib.setEnabled(true);
            boton_Alum.setEnabled(false);
            boton_Enc.setEnabled(true);
            boton_Prest.setEnabled(true);
        }
        else if(panelEnc.isVisible()){
            boton_Lib.setEnabled(true);
            boton_Alum.setEnabled(true);
            boton_Enc.setEnabled(false);
            boton_Prest.setEnabled(true);
        }else if(panelPrest.isVisible()){
            boton_Lib.setEnabled(true);
            boton_Alum.setEnabled(true);
            boton_Enc.setEnabled(true);
            boton_Prest.setEnabled(false);
        }
    }

